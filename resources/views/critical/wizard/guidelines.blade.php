            <h2 class="section-heading">Disaster Relief Assistance</h2>

            <p class="text-justify">
                The Ministry of Social Development and Family Services is the core social sector Ministry with the responsibility for coordination the implementation of Governments social and human development objectives. The Ministry is responsible for addressing the social challenges of poverty, social inequality and social exclusion. Emphasis is placed on developing and executing programmes and services that protect and assist the vulnerable and marginalized in our society.
            </p>
            
            <p class="text-justify">
                Financial assistance is payable to persons who experience loss of personal or household effects as a result of a natural disaster (e.g. fire, flood, landslide etc.); such assistance is provided under the Ministry’s General Assistance Grant Programmes.  Assistance is normally issued via an invoice order, however where the disaster is widespread Government policy will determine the method of payments (e.g. via cheque) and the amount payable.
            </p>

            <p class="text-justify">
                The services available from the MSDFS to victims of natural or man-made disasters upon completion of a needs assessment are as follows:
            </p>

            <ul>
                <li>Household items (to replace items such as stove, refrigerator, washing machine, living room set, dining room set, wardrobe, chest of drawer, bed and mattress, kitchen cupboard) – to a maximum of $10,000.00</li>
                <li>Clothing grant – maximum $1,000.00 per person</li>
                <li>School Supplies Grant – maximum of $1,000.00 (secondary school). $700.00 (primary school)</li>
                <li>House Rental Grant of $2,500 per month for an initial period of three months if alternative accommodation is required, this may be extended for a further period of three months depending on the needs of the family</li>
                <li>Emergency Food Support</li>
                <li>House Repairs up to $20,000</li>
                <li>Electrical House wiring up to $25,000</li>
                <li>Sanitary Plumbing up to $15,000.00</li>
                <li>Counselling services</li>
            </ul>

            <p class="text-justify">
                The disaster relief assistance is normally issued to persons whose household items are <strong>damaged beyond repairs</strong> and the amount approved for an item is based on an approved amount to replace a basic item. Only one person per household can apply.
            </p>
